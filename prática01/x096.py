# Crie um programa que tenha uma função Media(), que vai receber as 2 notas de
# um aluno e retornar a sua média para o programa principal.

def media(n1, n2) -> float:
    return (n1 + n2) / 2


media = media(7, 9)
print(f'Média: {media:.1f}')
